#Comparison operators
print("Comparison operators")
i=5
print(i==8)
print(i<8)
print(i>8)
print(i>=8)
print(i<=8)
print(i!=8)
#Logical operators
print("Logical operators")
c=True
d=False
print(c and d)
print(c or d)
#Identity operators
print("Identity operators")
print(5 is 5)
print(5 is not 5)
#Membership operators
list=[2,3,4,6,7,8,9,10]
print(10 in list)
